# 练习:定义一个函数实现对两个参数进行求和操作


def plus(a, b):  # 此处a,b为形参
    '''
    :param a:加数
    :param b:被加数
    :return:a+b
    '''
    return a + b


print(plus(3, 4))  # 此处a,b为实参
