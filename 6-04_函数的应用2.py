# 练习1: 写一个函数求三个数的和
def sum(a,b,c):
    sum = a+b+c
    return sum
s = sum(3,4,5)
print(s)

# 练习2: 写一个函数求三个数的平均值
def average(a,b,c):
    ave = (a+b+c)/3
    return ave
print(average(3,4,5))